resultado = 2
print(resultado)  # retorna como 2

resultado = 3
print(resultado)  # retorna como 3

resultado = 'próximo'
print(resultado)  # retorna como próximo

resultado = 2
resultado += resultado
resultado += 3  # resultado += 3 => resultado = resultado + 3 ...
resultado -= 1
resultado *= 4
resultado /= 2
resultado %= 6

print(resultado)  # retorna como 0
